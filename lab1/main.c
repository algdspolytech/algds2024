/*Задано число А и два вектора b[1..n] и c[1..n]. c[i] – стоимость i-го предмета, b[i] – его
вес. Предполагается, что 1< b[i]<=n. Найти множество I, являющееся подмножеством
множества {1,...,n}, такое, что SUMI b <=А, a SUMI c является максимальной.
Подсказка: Обозначим через F[k,y] величину наибольшей суммарной стоимости
элементов с номерами не большими k, суммарный вес которых не превышает y.*/ 
// Основная программа на языке C
#include <stdio.h>

int max(int a, int b) {
    return (a > b) ? a : b;
}

// Функция для нахождения максимальной суммы стоимостей
int findMaxCost(int A, int n, int b[], int c[]) {
    int F[101][1001] = {0}; // Матрица для хранения результатов
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= A; j++) {
            if (b[i] <= j) {
                F[i][j] = max(F[i-1][j], F[i-1][j - b[i]] + c[i]);
            } else {
                F[i][j] = F[i-1][j];
            }
        }
    }
    return F[n][A]; // Возвращаем результат
}

int main() {
    int A, n;

    // Ввод данных
    printf("Введите число A: ");
    scanf("%d", &A);

    printf("Введите размер векторов b и c: ");
    scanf("%d", &n);

    int b[100], c[100];

    printf("Введите элементы вектора b:\n");
    for (int i = 1; i <= n; i++) {
        scanf("%d", &b[i]);
    }

    printf("Введите элементы вектора c:\n");
    for (int i = 1; i <= n; i++) {
        scanf("%d", &c[i]);
    }

    // Вызов функции для нахождения максимальной суммы стоимостей
    int result = findMaxCost(A, n, b, c);

    // Вывод результата
    printf("Максимальная сумма стоимостей: %d\n", result);

    return 0;
}
